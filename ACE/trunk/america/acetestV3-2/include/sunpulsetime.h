struct ACEsunpulsetime
{
  float64  SPtime;  /* ACE sun pulse times in units of SC clock */
  uint8    flag;    /* 0 if calculated from sunpulse time tags (normal) */
                    /* 1 if calculated from spinphase clock values which */
		    /* are latched every minor frame (not normal) */
};
