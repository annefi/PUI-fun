C ***************************************************************

      FUNCTION ERF(X)

C ***************************************************************

C ***************************************************************
C ***                                                         ***
C *** returns the error function.                             ***
C *** from Numerical recipes, p. 164                          ***
C ***                                                         ***
C ***************************************************************


      IF(X.LT.0.)THEN
        ERF=-GAMMP(.5,X**2)
      ELSE
        ERF=GAMMP(.5,X**2)
      END IF
      RETURN
      END
